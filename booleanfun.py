# Test if two things are equal
print(5 == 5)

# Test if two things are not equal
print(5 != 5)

# Is one number smaller than another?
print(5 < 6)

# Is one number greater than the other number
print(5 > 6)

# Less than or equal to
5 <= 6

# Greater than or equal to
5 >= 6 # Evaluates to False





