# creating strings. double quotes work
my_string = "Hello, how are you today?"

# single quotes also work
my_string = 'Hello, how are you today?'

# you can use a single quote INSIDE double quotes
car_string = "Who's car is that?"

# you can use double quotes inside single quotes
car_string_2 = 'He asked, "Whose care is that"?'

# you can optionally escape a quote to include it in a string
car_string_3 = 'It\'s her car'